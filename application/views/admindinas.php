              <center>Tugas Pokok dan Fungsi Dinas Kesehatan</center>
              <br>
<p>Dinas Kesehatan mempunyai tugas pokok membantu Gubernur dalam melaksanakan kewenanganan wajib Pemerintahan dibidang kesehatan yang diserahkan kepada Pemerintah Daerah sesuai dengan PP Nomor 38/2007,
melaksanakan tugas dekonsentrasi dan tugas pembantuan dibidang kesehatan sesuai dengan PP nomor 7/ 2007, melaksanakan tugas lainnya yang dilimpahkan oleh Gubernur sesuai dengan tugas-tugas Dinas Kesehatan.</p>

<p>Dalam menyelenggarakan tugas sebagaimana tersebut dalam Pasal 3, di atas Dinas Kesehatan mempunyai fungsi :</p>
<p>a. Perumusan kebijakan teknis dibidang kesehatan sesuai dengan
kebijaksanaan yang ditetapkan Gubernur berdasarkan peraturan
perundang-undangan yang berlaku;</p>
<p>b. Koordinasi penyusunan program pembangunan kesehatan,
pengolahan data dan informasi kesehatan serta penelitian dan
pengembangan kesehatan;</p>
<p>c. Pelaksanaan pengendalian dan pencegahan penyakit,
penanggulangan wabah dan kejadian luar biasa (KLB) serta
pembinaan penyehatan lingkungan;</p>
<p>d. Penyusunan perumusan dan penjabaran kebijakan teknis pelayanan kesehatan dasar, rujukan dan khusus;</p>
<p>e. Pengendalian kefarmasian dan alat kesehatan, makanan dan
minuman serta obat tradisional;</p>
<p>f. Pemberian registrasi, sertifikasi, lisensi dan akreditasi terhadap
tenaga kesehatan, teknologi kesehatan dan gizi;</p>
<p>g. Pembinaan, pendayagunaan dan pengembangan sumber daya
kesehatan, teknologi kesehatan dan gizi;</p>
<p>h. Pembinaan promosi kesehatan, pemberdayaan kesehatan
masyarakan, Jaminan Pemeliharaan Kesehatan Masyarakat ( JPKM ) dan komunikasi publik;</p>
<p>i. Pembinaan, pelayanan, pengawasan, pengendalian,monitoring,
evaluasi dan pelaporan penyelenggaraan kesehatan;</p>
<p>j. Penyelenggaraan urusan kesekretariatan dinas;</p>
<p>k. Melaksanakan fungsi lainnya yang dilimpahkan oleh Gubernur
sesuai dengan fungsi Dinas Kesehatan;</p></div>
